    private void BtnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnGuardarMouseClicked
        if (TxtVlrUnidad.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe digitar el Valor del producto", "Mensaje del sistema", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            if (TxtNomProducto.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Debe digitar un nombre", "Mensaje del sistema", JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                ProductoController accion = new ProductoController();

                id = Integer.parseInt(TxtIdProducto.getText());
                nombre = TxtNomProducto.getText();
                vlrunidad =  Double.parseDouble(TxtVlrUnidad.getText());
                Producto producto = new Producto(id,nombre, vlrunidad);

                int insert = accion.registrar(producto);

                if (insert == 1){
                    mostrardatos();
                    JOptionPane.showMessageDialog(null, "Datos guardados exitosamente", "Mensaje del sistema", JOptionPane.INFORMATION_MESSAGE);
                }
                else{
                    JOptionPane.showMessageDialog(null, "No fue posible guardar los datos", "Mensaje del sistema", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_BtnGuardarMouseClicked

 
