    private void mostrardatos() {
        ProductoController accion = new ProductoController();
       modelo = (DefaultTableModel) datos.getModel();
       datos.setModel(modelo);
        
        List<Producto> lista = accion.listar();
        Object[] objeto = new Object[3];
        for (int i = 0; i < lista.size(); i++) {
            objeto[0] = lista.get(i).getIdProducto();
            objeto[1] = lista.get(i).getNomProducto();
            objeto[2] = lista.get(i).getVlrUnidad();
            
            modelo.addRow(objeto);
        }
        datos.setRowHeight(35);
        datos.setRowMargin(10);
    
    }
